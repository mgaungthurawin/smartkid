မွေးကင်းစအရွယ်မှာ မိခင်နို့က လွဲလို့ ဘယ်အရာမှမတိုက်ကျွေးရပါဘူး။ ရေတောင်မတိုက်သင့်သေးပါဘူး။ဒါကြောင့် ကလေးကို ဆာတိုင်းမှာ မိခင်နို့ရည်ကိုတိုက်ပေးဖို့လိုအပ်လှပါတယ်။ ကလေးဟာ ငိုတဲ့ အခါ နို့ဆာလို့ငိုတာလား ၊ တခုခုကြောင့်ငိုတာလားဆိုတာကိုလည်းခွဲခြားသိဖို့ လိုအပ်ပါတယ်။ နို့ဆာလို့ငိုတဲ့ အခါနို့တိုက်ပြီးတိုင်းမှာလည်း လေထုတ်ပေးဖို့လိုအပ်ပါတယ်။ဒါမှသာ ကလေးက လေမနာလည်းဖြစ်ပါတယ်။ မိခင်နဲ့ ကလေးရဲ့ ဆက်သွယ်ချက်တွေကအရေးကြီးပါတယ် ကလေးကို အမြဲရင်ခွင်ပိုက်ထားပေးခြင်းကနေကလေးရဲ့ စိတ်ကို ပိုပြီးသိလာနိုင်ပါတယ်။အဲ့လိုသိလာတဲ့ အခါကျတော့ နို့ဆာလို့ငိုတာလား ၊ ရှုးရှုးပေါက်ချလို့ငိုတာလားဆိုတာတွေကစလို့ အရင်ဆုံးသိမှာကတော့ မေမေပဲဖြစ်ပါလိမ့်မယ်။ အဓိကပြောချင်တာကတော့ နို့တိုက်ပါ ဆာတိုင်းတိုက်ပါ မိခင်နို့တစ်မျိုးထဲသာတိုက်ပါ မွေးကင်းစအရွယ်မှာ။ 



၂လသား အရွယ်

