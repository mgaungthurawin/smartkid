        သ င့ ် ကေ လး ရဲ 5 အရိုး တွေ ဟာ ြ ကီး ထွား ဆဲ ြ ဖစ် တာေ ြ ကာ င့ ် အ ရွယ် ရောက် ြ ပီး သူေ တွ မှာ မေ တွ 4 ရတဲ့ နောက် ထပ် အရိုး ကျ ိုး ြ ခင်း ပုံ စံ တစ် မျ ိုး ပါ သူ 4 အ ဖို 4 ခံစား ရန ိုင် ချေ ရှိ ပါ တယ်. ဒါ ဟာေ တာ့ အရိုး ၏ အ စွန်း မျ ား ရှိ အရိုး ဆက် လက် ြ ကီး ထွား မှု အ တွက် ထိန်း ချ ုပ် အ လုပ် လုပ် ဆောင် ပေး သ ည့ ် ဖွံ ြ ဖိုး ဆဲ တစ် ရှု း ဧ ရိ ယာမျ ား ထိ ခိုက် အနာ တ ရြ ဖစ် ြ ခင်း ပါဘဲ. အရိုး ၏ ယင်း အ စိတ် အ ပိုင်း ကျ ိုး ြ ပီး နောက် အချ ိုး ကျ မှန် ကန် စွာ ြ ပန် လည် ကောင်း မွန် လာ ြ ခင်း မ ရှိ ပါ က ယင်း အရိုး ဟာ စောင်း. ြ ကီး ထွား ြ ခင်း သို 4 မ ဟုတ် ခန ္ဓာ ကိုယ် ရှိ အြ ခား အရိုး မျ ား ထက် နေ ှ း ကွေး. ဖွံ 4 ြ ဖိုး ြ ခင်း တို 4 ြ ဖစ်န ိုင် ပေ သည်. ကံ ဆိုး သ ည့ ် အချ က ် မှာ ထိ ခိုက် အနာ တ ရြ ဖစ် ြ ပီး တစ်န ှ စ ် (သို 4) တစ်န ှ စ ် ကျေ ာ ် သည် အထိ ယင်း အရိုး ၏ ြ ကီး ထွား မှု န ှ င့ ် ပတ် သက် သ ည့ ် သက် ရောက် မှု ကို မြ မင်န ိုင် ြ ခင်း ပါဘဲ. သို 4 ြ ဖစ်. ထိုသို 4 သော အရိုး ကျ ိုး ြ ခင်း ြ ဖစ် စဉ် မျ ား န ှ င့ ် ပတ် သက် ြ ပီး နည်း လမ်း မကျ သော ဖွံ 4 ြ ဖိုး မှု မ ရှိေ စ ဖို 4 (12) လ မှ (18) လ အထိ ကေ လး အထူးကု ၏ ညွန် ြ ကား ချ က ် အ တိုင်း ဂရု တ စိုက် ြ ပု မူေ ဆာင် ရွက် ရ မှာ ြ ဖစ် ပါ တယ်.

         <br>

        ဤသို 4 ဖွံ ြ ဖိုး ဆဲ တစ် ရှု း ဧ ရိ ယာမျ ား အား ထိ ခိုက် အနာ တ ရြ ဖစ် မှု ပါ ဝင် သ ည့ ် အရိုး ကျ ိုး ြ ခင်း ြ ဖစ် စဉ် မျ ား ၌ နောင် ြ ဖစ် လာ မ ည့ ် အရိုး ြ ကီး ထွား ြ ခင်း ဆိုင် ရာ ြ ပ သ နာမျ ား ၏ အန ္တ ရာယ် ကို လှေ ျ ာ့ ချ ရန် တစ် ခါ တစ် ရံ ခွဲ စိတ် ကုသ မှု   ြ ပု လုပ် ဖို 4 လို အပ် ပါ တယ်. တံေ တာင် ဆစ် အနား မှာ ြ ဖစ် တဲ့ အရိုး ကျ ိုး ြ ခင်း တွေ ဟာ မ ြ ကာ ခဏ ကွေး သွား သ ည့ ် အေ န အ ထား တစ် ခုကို ြ ဖစ် စေ ရင်း ပုံ မှန် မ ဟုတ် သ ည့ ် ပုံ စံ ြ ဖ င့ ် လက် မောင်း ကို သက် သာ သွား စေ တတ် ပါ တယ် . ဤ အန ္တ ရာယ် ကို လှေ ျ ာ့ ချ ဖို 4 အ တွက် ယခုလို အေ ြ ခ အေ န အေ တာ ်မ ျ ား မျ ား မှာ ခွဲ စိတ် ကုသ မှု လို အပ် ပါ တယ် . တံေ တာင် ဆစ် အနီး အရိုး ကျ ိုး ြ ခင်း မျ ား ရှိ သ ည့ ် ကေ လး မျ ား အား အရိုး အထူးကု တစ် ဦး ဆီ ညွှ န ် ပေး န ိုင် ပါ တယ် .

         <br>

        အရိုး ကျ ိုး ြ ခင်း ကို ကျ ိုး ပဲ့ သွား သ ည့ ် အရိုး အ စွန်း မျ ား နေ ရာ အ တိုင်း ရှိေ န က “değiştirmemiş” န ှ င့ ် အရိုး အ စွန်း မျ ား ကွဲ ကွာ ပုံပျ က ် ြ ပီး နေ ရာ အ တိုင်း မ ရှိေ တာ့ ပါ က “yerinden” ဟူ. လည်း အမျ ိုး အစား ခွဲန ိုင် ပါ တယ်. “Açık Kırılma” Com Com Com “Bileşik Kırık” မှာ အရိုး ဟာ အေ ရြ ပား ထဲ မှ အြ ပင် သို့ ထိုး ထွက် မှ တယ်။ Closed ရြ ပား ဟာ နဂို ပုံ စံ အ တိုင်း မပျ ဆို ရင် တော့ ဒါဟာ “Kapalı Kırılma” ပါ။

    </p>

         

         

        <b>အရိုး ကျ ိုး ြ ခင်န ှ င့ က ် ြ ပ သေ င်္က တမျ ား န ှ င့ ် ရော ဂါ န ှ င့ ် ရော ဂါ လ က ှ ် ် ဂါ ား က ှ င့ ်</b>

        <p>ဘယ် အချ ိန် မှာ အရိုး ကျ ိုး သလဲဆိုတာ ကို ေ ြ ပာ ဖို 4 ဆိုတာ အြ မဲ တမ်း တော့ မ လွယ် ကူ လှ ပါဘူး. အထူး သြ ဖ င့ ် သ င့ ် ကေ လး ဟာ သူ ဘာ ကို ခံစား နေ ရ တယ် ဆိုတာ ထုတ် ေ ြ ပာ ဖို 4 အ လွန် ငယ် လွန်း နေ တဲ့ အ ခါမျ ိုး မှာ တစ် ကယ် ကို မ လွယ် ပါဘူး. အြ မဲ လို လို အရိုး ကျ ိုး သ ည့ ် အခါ ရောင် ရမ်း နြေ ခင်း ကို သင် တွေ 4 ရပါ လိ မ့ ် မယ်. ြ ပီး တော့ ကေ လး ဟာ ပီး အနာ တ ရြ ဖစ် ထားတဲ့ ေ ြ တ၊၊ လက် ကို မ လှု ပ ် ရှား န ိုင် တာ (သို 4) မ လှု ပ ် ရှား ချ င ် တာမျ ိုး ရှိ ပါ လိ မ့ ် မယ်. သို 4 သော ် ြ ငား လည်း ကေ လး ေ ြ ခေ ထာက် လှု ပ ် ရှား န ိုင် သည် ကို ြ က ည့ ် ရုံ ြ ဖ င့ ် အရိုး မကျ ိုး ဘူး ဟု လည်း အေ ြ ခ အေ န တိုင်း အ တွက် မ ဆိုန ိုင် ပါဘူး. မည် သ ည့ ် အချ ိန် မ ဆို အရိုး ကျ ိုး တာ ြ ဖစ်န ိုင် တယ် လို 4 သင် ယူဆ ပါ က သ င့ ် ကေ လး အထူးကု အား ချ က်ခ ျ င ် း အ သိေ ပး အေ ြ ကာင်း ြ ကား ပါ.</p>

         

        <b>အိမ် တွင်း ကု ထုံး</b>
        <p>
        င့ ် ကေ သ လး ကို ကေ လး အထူး ကုေ ဆး ခန်း, အေ ရး ပေ ါ် ခန်း (သို 4) အေ ရး ပေ ါ်ေ ဆး ကုသ မှု စင် တာ ဆီ မေ ခ ါ် သွား မီ အလျ င ် ဒဏ် ရာ ကို မ လို အပ် တဲ့ လှု ပ ် ရှား မှု မ ရှိေ စ ရန် လက် တလော လွယ် လွယ် ကူ ကူ ရန ိုင် မ ယ့ ် သိုင်း စရာ တစ် ခု ခု (သို 4) လိပ် ထား သော သ တင်း စာ, မ ဂ္ဂ ဇင်း တစ် ခု ခုကို ြ ကိုး သိုင်း တစ် ခု အြ ဖစ် အ သုံး ြ ပု ပါ .
        <br>
         

        ဆရာ ဝန်န ှ င့ ် အ ရင် မ တိုင် ပင် ဘဲ ကေ လး အား တစ် ခု ခုေ သာက် သုံး စေ ဖို 4 သော ် 4 င ် း, နာကျ င ် မှု သက် သာေ စ င့ သော ် ် ၄ င ် း၊ ပါး စပ် ထဲ ဘာ ကို မှ မ ထ ည့ ် ပေး ပါ နဲ့။ သ င့ ် ကေ လး ဟာ နည်း နည်း ပို ြ ကီး တဲ့ အ ရွယ် ြ ဖစ် မယ် ဆို ရင် တော့ အေး ြ မ တဲ့ ပု ဝါ တစ် ထည် သို 4 မ ဟုတ် ရေ ခဲ ထုပ် တစ် ထုပ် (soğuk paket) ကို ဒဏ် ရာ ရ သ ည့ ် နေ ရာ အေ ပ ါ် တင် ြ ပီး နာကျ င ် မှု ကို လျေ ာ့ နည်း သွား စေ ဖို 4 အ သုံး ြ ပုန ိုင် ပါ တယ်. အေ အး ဓါတ် လွန် ကဲ မှု ဟာ မွေး ကင်း စ အ ရွယ် ကေ လး မျ ား န ှ င့ ် လမ်း လှေ ျ ာက် တတ် စ ကေ လး မျ ား ၏ န ူး ညံ့ သ ည့ ် အေ ရြ ပား ကို အနာတရ ြ ဖစ် စေန ိုင် တာေ ြ ကာ င့ ် ယင်း အ ရွယ် ကေ လး မျ ား အ တွက် တော့ ရေ ခဲ ကို အ သုံး မြ ပု ပါ နဲ 4.

         <br>

        သ င့ ် ကေ လး ေ ြ ခေ ထာက် ကျ ိုး သွား တယ် ဆိုပါစို 4 .... သူ 4 ကို သင် ကိုယ် တိုင် ရွှေ 5 ဖို 4 မ ြ ကိုး စား ပါ နဲ 4. လူနာ တင် ယာဉ် ခေ ါ်ြ ပီး ကေ လး ၏ သယ် ယူ ပို 4 ဆောင် ရေး ကို ဆေး ဘက် အ ကူမျ ား အား ြ ကီး ြ ကပ် ညွှ န ် ြ ကား စေ ရင်း ကေ လး ကို ြ ဖစ်န ိုင် သ မှ ျ သက် တော င့ ် သက် သာ ရှိေ အာင် ြ ပု လုပ် ပေး ပါ.

         </p>

         

        <b>ဆေး ရုံေ ဆး ခန်း သုံး ကု ထုံး</b>

        <p>
